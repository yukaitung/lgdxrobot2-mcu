void Broadcast_Status()
{
	static uint8_t msg[128] = {'\0'};
	static int index = 0;
	static uint32_t temp = 0;
	msg[0] = 0xAA;
	index = 5;
	for(int i = 0; i < WHEEL_COUNT; i++)
	{
		temp = Float_To_Uint32(MOTOR_Get_Target_Velocity(i));
		msg[index++] = (temp & 4278190080) >> 24;
		msg[index++] = (temp & 16711680) >> 16;
		msg[index++] = (temp & 65280) >> 8;
		msg[index++] = temp & 255;
	}
	for(int i = 0; i < WHEEL_COUNT; i++)
	{
		temp = Float_To_Uint32(MOTOR_Get_Velocity(i));
		msg[index++] = (temp & 4278190080) >> 24;
		msg[index++] = (temp & 16711680) >> 16;
		msg[index++] = (temp & 65280) >> 8;
		msg[index++] = temp & 255;
	}
	for(int i = 0; i < 3; i++)
	{
		for(int j = 0; j < WHEEL_COUNT; j++)
		{
			temp = Float_To_Uint32(MOTOR_Get_PID(i, j));
			msg[index++] = (temp & 4278190080) >> 24;
			msg[index++] = (temp & 16711680) >> 16;
			msg[index++] = (temp & 65280) >> 8;
			msg[index++] = temp & 255;
		}
	}
	for(int i = 0; i < 2; i++)
	{
		temp = ina219VoltageValue[i];
		msg[index++] = (temp & 4278190080) >> 24;
		msg[index++] = (temp & 16711680) >> 16;
		msg[index++] = (temp & 65280) >> 8;
		msg[index++] = temp & 255;
	}
	for(int i = 0; i < 2; i++)
	{
		temp = MOTOR_Get_E_Stop_Status(i);
		msg[index++] = (temp & 4278190080) >> 24;
		msg[index++] = (temp & 16711680) >> 16;
		msg[index++] = (temp & 65280) >> 8;
		msg[index++] = temp & 255;
	}
	// Final: Size
	temp = index;
	msg[1] = (temp & 4278190080) >> 24;
	msg[2] = (temp & 16711680) >> 16;
	msg[3] = (temp & 65280) >> 8;
	msg[4] = temp & 255;
	CDC_Transmit_FS(msg, index);
}

void Update_Ina219()
{
	// Initalise INA219 Communication
	HAL_I2C_Master_Transmit_IT(&hi2c1, ina219Addr[currentIna219], ina219VoltageRegister, 1);
}

void HAL_I2C_MasterTxCpltCallback(I2C_HandleTypeDef *hi2c)
{
  // Sent Data To INA219
	if(hi2c->Instance == hi2c1.Instance) 
	{
		HAL_I2C_Master_Receive_IT(&hi2c1, ina219Addr[currentIna219], ina219VoltageData, 2);
	}
}

void HAL_I2C_MasterRxCpltCallback (I2C_HandleTypeDef *hi2c)
{
  // Receive Data From INA219
	if(hi2c->Instance == hi2c1.Instance) 
	{
		ina219VoltageValue[currentIna219] = (ina219VoltageData[0] << 8 | ina219VoltageData[1]) >> 3;
		if(ina219VoltageValue[actuator_battery] < 2000) // Lower than 8V which lower than 4 * 18650 lowest vlotage (2 * 4)
			MOTOR_Set_Hardware_E_Stop(1);
		else
			MOTOR_Set_Hardware_E_Stop(0);
		currentIna219 = currentIna219 == actuator_battery ? logic_battery : actuator_battery;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	static int tim2_count = 0; 
	if(htim->Instance == htim2.Instance)
	{
		// About 10ms
		tim2_count++;
		if(tim2_count >= TIMER2_COUNT_MAX)
		{
			tim2_count = 0;
			MOTOR_PID();
			Broadcast_Status();
			Update_Ina219();
		}
	}
}

